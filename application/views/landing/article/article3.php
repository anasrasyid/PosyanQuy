                    <br><br>

                    "Biasanya semakin tinggi usia anak, ibu-ibu semakin malas membawa anaknya ke Posyandu karena menganggap bahwa imunisasinya sudah sukses dan lengkap, padahal semakin rutin anak dibawa ke Posyandu, maka tumbuh kembangnya semakin terpantau," kata Naisyah pada Rakyatku.com, Jumat (7/10/2016).

                    <br><br>

                    Naisyah menambahkan, apabila anak rutin dibawah ke Posyandu, tentu bisa melakukan diagnosis dan penanganan kepada anak jika mengalami gangguan kesehatan.

                    <br><br>

                    "Ada banyak sekali manfaat yang bisa didapatkan jika bayi dibawah Posyandu secara rutin setiap bulan, karena disana kita bisa mengetahui apakah balita tumbuh sehat, bisa mencegah gangguan pertumbuhan balita, ibu mendapat penyuluhan gizi pertumbuhan balita," paparnya.

                    <br><br>

                    Selain itu, lanjut Naisyah, pihak Posyandu juga bisa segera merujuk anak ke Puskesmas bila mengalami sakit demam, batuk, pilek, dan diare, atau ketika bulan berat badan anak tidak naik dan berada dibawah garis merah.
